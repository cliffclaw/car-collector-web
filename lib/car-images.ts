export function getCarImage(make: string, model: string = ''): string {
  const searchKey = `${make} ${model}`.toLowerCase().trim();
  
  // Try exact match first
  if (carMakeImages[searchKey]) {
    return carMakeImages[searchKey];
  }
  
  // Try just make
  if (carMakeImages[make.toLowerCase()]) {
    return carMakeImages[make.toLowerCase()];
  }
  
  // Try partial match
  for (const key of Object.keys(carMakeImages)) {
    if (searchKey.includes(key) || key.includes(searchKey)) {
      return carMakeImages[key];
    }
  }
  
  return carMakeImages['default'];
}